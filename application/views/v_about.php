			<body>
			<p align="justify">Bandar Udara Malikussaleh terletak di Desa Pinto Makmur Kecamatan Muara Batu Kabupaten Aceh Utara 
   Provinsi Aceh yang berjarak 30 kilometer (km) dari Kota Lhokseumawe. 
   Bandara ini mulai dibangun tahun 1973 oleh PT Arun Natural Gas Liquefaction (NGL) 
   yang adalah anak perusahaan dari PT Pertamina.
   Bandara ini dirancang untuk Bandara Khusus yang difungsikan sebagai sarana transportasi
   bagi karyawan perusahaan guna kelancaran pembangunan dan pengelolaan Kilang Arun yang menghasilkan gas alam cair. 
   Pada saat itu Bandara Malikussaleh secara rutin melayani penerbangan charter milik Pelita Air dengan penerbangan 
   perdana pada tahun 1980-an, menggunakan pesawat jenis Beechrhaft, dan Dash 7 dengan rute Medan (Polonia) - Lhokseumawe (Malikussaleh). 
   Pesawat hanya khusus digunakan oleh karyawan perusahaan PT Arun NGL.</p>


   <p align="justify">Pada tahun 2003, Bandara Khusus ini mulai melayani penerbangan komersil
	    menggunakan pesawat jenis Boeing 737 Jatayu dengan rute Medan (Polonia) - Lhokseumawe 
		(Malikussaleh) yang masih dikelola oleh PT Arun NGL. Penerbangan ini pun hanya beroperasi selama 
		dua bulan kemudian vakum kembali sampai dengan tahun 2008. Pada tahun 2008 Pertamina menyerahkan aset-aset 
		dan kepemilikan bandara ini kepada Kementerian Keuangan, dan selanjutnya Kementerian Keuangan menyerahkan 
		pengelolaannya kepada Pemerintah Kabupaten Aceh Utara. Pada tahun 2010 bandara ini kembali dioperasionalkan 
		oleh Pemerintah Kabupaten Aceh Utara untuk melayani penerbangan komersil dengan Maskapai Wings Air menggunakan 
		jenis pesawat ATR 72 dengan rute Medan (Kualanamu) - Lhokseumawe (Malikussaleh) (PP) setiap hari.</p>

		<p align="justify">Selanjutnya pada tahun 2015 penerbangan bertambah dengan masuknya maskapai Garuda Indonesia 
			dengan jenis pesawat, rute serta  jadwal penerbangan yang sama pula. Pada tahun 2016 Kementerian Keuangan 
			menyerahkan kepemilikan Bandara beserta aset-aset bandara ini kepada Pemerintah Kabupaten Aceh Utara berupa 
			Tanah seluas +/- 681.503 M2 termasuk seluruh fasilitas di dalamnya yang terdiri dari fasilitas sisi udara 
			berupa Landas Pacu (Runway), Taxiway A, Taxiway B, dan Apron yang seluruhnya dibangun dengan konstruksi aspal
			 hotmix. Sedangkan untuk fasilitas sisi darat antara lain berupa Gedung Terminal, Gedung Kantor, Gedung Genset
			 , Gedung PKP-PK, Gedung Workshop, Gedung Tower dengan 3 lantai, dan Lahan Parkir.</p>

			 <p align="justify">Sejak pelimpahan pengoperasian bandara dari mulai PT Arun NGL hingga ke Pemerintah 
				 Kabupaten Aceh Utara, kondisi bandara menurun drastis dan memprihatinkan, ditambah kurangnya dukungan
				  dana menyebabkan kondisi infrastruktur bandara tidak terawat dengan baik, maka Pemerintah Kabupaten 
				  Aceh Utara berinisiatif menyerahkan bandara ini kepada Direktorat Jenderal Perhubungan Udara. 
				  Dari inisiatif tersebut, maka pada tanggal 1 November 2018 telah dilakukan penandatanganan perjanjian 
				  antara kedua belah pihak, yaitu antara Pemerintah Kabupaten Aceh Utara dengan Direktorat Jenderal 
				  Perhubungan Udara Kementerian Perhubungan berupa Berita Acara Pengalihan Pengoperasian Bandar Udara
				   Malikussaleh dari Pemerintah Kabupaten Aceh Utara kepada Kementerian Perhubungan untuk dipergunakan 
				   dalam tugas-tugas operasional.</p>

				   <p align="justify">Sejalan dengan telah dilakukannya penandatanganan perjanjian tersebut di atas,
					    Kementerian Perhubungan selanjutnya menerbitkan Keputusan Direktur Jenderal Perhubungan Udara
						 Nomor: KP 396 Tahun 2018 Tanggal 21 Desember 2018 Tentang Satuan Pelayanan Bandar Udara
						  Malikussaleh, yang dalam diktum-diktum penetapannya menyebutkan bahwa “Menetapkan Organisasi 
						  Pengoperasian Bandar Udara Malikussaleh sebagai Satuan Pelayanan (Satpel) Bandar Udara dan berada 
						  dibawah dan bertanggung jawab kepada Kepala Kantor Unit Penyelenggara Bandar Udara (UPBU) Rembele”
						  . Memperhatikan Keputusan Direktur Jenderal Perhubungan Udara tersebut, maka UPBU Rembele selaku 
						  penanggung jawab Satpel Bandar Udara Malikussaleh mulai mengoperasikan dan mengelola bandara
						   tersebut terhitung mulai tanggal 1 Januari 2019.</p>

						   <p align="justify">Pada tanggal 15 Agustus 2019, sesuai dengan terbitnya Peraturan Menteri Perhubungan Nomor PM 56 Tahun 2019 tentang Perubahan Keempat Atas Peraturan Menteri Perhubungan Nomor PM 40 Tahun 2014 Tentang Organisasi Dan Tata Kerja Kantor Unit Penyelenggara Bandar Udara, Satpel Bandar Udara Malikussaleh berubah status menjadi UPBU Kelas III Malikussaleh. Akhirnya pada tahun 2021 untuk pertamakalinya UPBU Malikussaleh memiliki dan mengelola Anggaran Pendapatan dan Belanja Negara (APBN) tersendiri sesuai Daftar Isian Pelaksanaan Anggaran (DIPA) Nomor SP.DIPA 022.05.2.649049/2021 tanggal 23 November 2020.</p>
